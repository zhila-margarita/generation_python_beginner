"""Упаковка дубликатов 🌶️
На вход программе подается строка текста, содержащая символы. Напишите программу, которая упаковывает последовательности одинаковых символов заданной строки в подсписки.

Формат входных данных
На вход программе подается строка текста, содержащая символы, разделенные символом пробела.

Формат выходных данных
Программа должна вывести указанный вложенный список."""

# Превращаем введенную строку в список символов
text = list(input().split()) 

# Инициализируем текущую группу (кладем туда самый первый символ)
cur_res = [text[0]] 
# Список, где будут лежать все готовые группы
total = []

# Идем по списку, начиная со второго элемента
for i in range(1, len(text)):
    # Если текущий символ такой же, как предыдущий — добавляем в группу
    if text[i] == text[i - 1]:
        cur_res.append(text[i])
    # Если символ изменился:
    else:
        # 1. Сохраняем собранную группу в общий список
        total.append(cur_res)
        # 2. Начинаем новую группу с текущего символа
        cur_res = [text[i]]

# После цикла добавляем последнюю группу, которая осталась в cur_res
total.append(cur_res)

# Выводим итоговый список списков
print(total)