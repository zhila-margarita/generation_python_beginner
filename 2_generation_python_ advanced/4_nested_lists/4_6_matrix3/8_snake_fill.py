"""Заполнение змейкой
На вход программе подаются два натуральных числа n и m. Напишите программу, которая создает матрицу размером n×m, заполнив ее "змейкой" в соответствии с образцом.

Формат входных данных
На вход программе на одной строке подаются два натуральных числа n и m – количество строк и столбцов в матрице.

Формат выходных данных
Программа должна вывести указанную матрицу в соответствии с образцом.

Примечание. Для вывода элементов матрицы как в примерах отводите ровно 3 символа на каждый элемент. Для этого используйте строковый метод ljust(). Можно обойтись и без ljust(), система примет и такое решение. 😇


Примечание. Для вывода элементов матрицы как в примерах отводите ровно 3 символа на каждый элемент.
Для этого используйте строковый метод ljust(). Можно обойтись и без ljust(), система примет и такое решение. 😇
"""


n, m = [int(el) for el in input().split()]
matrix = []  # Инициализируем итоговую матрицу

for i in range(n):
    start = i * m + 1  # Вычисляем начальное число для текущей строки
    if i % 2 == 0:
        # Для четных строк (0, 2, 4...) заполняем слева направо
        matrix.append(list(range(start, start + m)))
    else:
        # Для нечетных строк (1, 3, 5...) заполняем справа налево
        matrix.append(list(range(start + m - 1, start - 1, -1)))


for row in matrix:
    # Форматируем числа по 3 символа и печатаем без лишних пробелов
    print(*(str(el).ljust(3) for el in row), sep="")
