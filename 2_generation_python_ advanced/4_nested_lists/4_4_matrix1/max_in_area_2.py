"""Напишите программу, которая выводит максимальный элемент в заштрихованной области квадратной матрицы. (2 треугольника: левый - под главной диагональной и над побочной, правый = под побочной и над главной



Формат входных данных: на вход программе подается натуральное число n – количество строк и столбцов в матрице, затем элементы матрицы (целые числа) построчно через пробел.

Формат выходных данных: программа должна вывести одно число – максимальный элемент в заштрихованной области квадратной матрицы.

Примечание. Элементы диагоналей также учитываются."""

n = int(input())
# Создаем матрицу через генератор списков
matrix = [[int(el) for el in input().split()] for _ in range(n)]

# За начальный максимум берем первый элемент области
max_val = matrix[0][0]

for i in range(n):
    # Левый треугольник: до меньшего индекса диагонали включительно
    for j in range(min(i, n - 1 - i) + 1):
        if matrix[i][j] > max_val:
            max_val = matrix[i][j]
            
    # Правый треугольник: от большего индекса диагонали до конца строки
    for j in range(max(i, n - i - 1), n):
        if matrix[i][j] > max_val:
            max_val = matrix[i][j]

print(max_val)
