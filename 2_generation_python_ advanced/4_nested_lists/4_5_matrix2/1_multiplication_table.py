"""Таблица умножения
На вход программе подаются два натуральных числа n и m – количество строк и столбцов в матрице.
Создайте матрицу mult размером n×m и заполните ее таблицей умножения по формуле mult[i][j] = i * j.

Формат входных данных: на вход программе на разных строках подаются два числа n и m – количество строк и столбцов в матрице.

Формат выходных данных: программа должна вывести таблицу умножения отводя на вывод каждого числа ровно 3 символа (для этого используйте строковый метод ljust()).
"""

# Читаем количество строк и столбцов
row, column = int(input()), int(input())

# Создаем таблицу умножения через вложенный генератор
mult = [[r * c for c in range(column)] for r in range(row)]

# Построчно выводим результат
for r in range(row):
    for c in range(column):
        # Выравниваем числа по левому краю (ширина 3), чтобы колонки были ровными
        print(str(mult[r][c]).ljust(3), end=" ")
    print()  # Переход на новую строку после завершения ряда
