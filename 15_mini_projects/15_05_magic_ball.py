answers = [
    # Положительные
    "Бесспорно ✅",
    "Предрешено 🔮",
    "Никаких сомнений 🌟",
    "Определённо да 👍",
    "Можешь быть уверен в этом 💯",
    # Нерешительно положительные
    "Мне кажется — да ✨",
    "Вероятнее всего 📈",
    "Хорошие перспективы 🌤️",
    "Знаки говорят — да 🌠",
    "Да ✅",
    # Нейтральные
    "Пока неясно, попробуй снова 🌫️",
    "Спроси позже ⏳",
    "Лучше не рассказывать 🤫",
    "Сейчас нельзя предсказать 🌀",
    "Сконцентрируйся и спроси опять 🧘‍♂️",
    # Отрицательные
    "Даже не думай ⛔",
    "Мой ответ — нет ❌",
    "По моим данным — нет 📉",
    "Перспективы не очень хорошие 🌥️",
    "Весьма сомнительно 🤨",
]

thinking_process = [
    "Настраиваюсь на частоту судьбы...",
    "Советуюсь со звёздами...",
    "Перемешиваю вероятности...",
    "Заглядываю в чертоги будущего...",
    "Считываю информационное поле...",
    "Магические потоки приходят в движение...",
]

print("Привет, Мир! Я магический шар, и я знаю ответ на любой твой вопрос.")
name = input("Как тебя зовут? ").capitalize()
print(f"Привет, {name}!")

while True:
    print()
    print("Давай разбираться с твоим вопросом.")
    question = input("Задай его так, чтобы я мог ответить да или нет: ")

    # Выбираем случайную фразу «раздумья»
    print()
    print(random.choice(thinking_process))
    time.sleep(1.5)
    print("Просчитываю варианты...")
    time.sleep(1)

    # Выдаем ответ
    print(f"🔮 Ответ для тебя: {random.choice(answers)}")

    # Короткая пауза перед предложением нового вопроса
    time.sleep(1)

    choice = input("\nХочешь задать еще один вопрос? (да/нет): ").lower()

    # Если пользователь ввел "нет" (в любой раскладке), выходим из цикла
    if choice in ("нет", "ytn", "no", "stop", "стоп"):
        print(f"\nДо встречи, {name}! Пусть звёзды светят тебе ярко. ✨")
