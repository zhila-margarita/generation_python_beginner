"""Снежинка
На вход программе подается нечетное натуральное число n. Напишите программу, которая создает матрицу размером n×n заполнив её символами .
Затем заполните символами * среднюю строку и столбец матрицы, главную и побочную диагональ матрицы. Выведите полученную матрицу на экран, разделяя элементы пробелами.

Формат входных данных: на вход программе подается нечетное натуральное число (n≥3) — количество строк и столбцов в матрице.

Формат выходных данных: Программа должна вывести матрицу в соответствии с условием задачи.
"""

n = int(input())
#создаем матрицу из точек
matrix = [["."] * n for _ in range(n)]

#определяем середину для центрального столбца и строки
mid = n // 2

#меняем нужные элементы на "*", не перебирая каждый элемент матрицы
for i in range(n):
    # главная диагональ
    matrix[i][i] = "*"
    # побочная диагональ
    matrix[i][n - i - 1] = "*"
    # центральный столбец
    matrix[i][mid] = "*"
    # центральная строка
    matrix[mid][i] = "*"

#вывод через пробел
for row in matrix:
    print(" ".join(row))
