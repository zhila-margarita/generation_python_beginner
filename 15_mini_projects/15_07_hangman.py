def get_word():
    word = random.choice(word_list).upper()
    return word

def display_hangman(tries):
    stages = [
    '❌❌❌❌❌❌',
    '❌❌❌❌❌💚',
    '❌❌❌❌💚💚',
    '❌❌❌💚💚💚',
    '❌❌💚💚💚💚',
    '❌💚💚💚💚💚',
    '💚💚💚💚💚💚'
    ]
    return stages[tries]

def play(word):
    # 1. Инициализация переменных
    word_completion = "_" * len(word)
    guessed = False
    guessed_letters = []
    guessed_words = []
    tries = 6

    # 2. Приветствие
    print("Давайте играть в угадайку слов!")
    print(display_hangman(tries))
    print(f"\n🎮 Загадано слово из {len(word)} букв")
    print(word_completion)
    print() # Пустая строка для красоты

    # 3. Основной цикл игры
    while not guessed and tries > 0:
        guess = input("Введите букву или слово целиком: ").upper()

        # Защита от дурака: проверяем, что это только буквы
        if not guess.isalpha():
            print("Это не буква. Попробуй еще раз.")
            continue

        # ВАРИАНТ 1: Пользователь ввел ОДНУ букву
        if len(guess) == 1:
            if guess in guessed_letters:
                print(f"⚠️ Вы уже вводили букву: {guess}")
            elif guess not in word:
                print(f"❌ Буквы '{guess}' нет в слове.")
                tries -= 1
                guessed_letters.append(guess)
            else:
                print(f"✅ Отлично! Буква '{guess}' есть в слове!")
                guessed_letters.append(guess)
                
                # Обновляем слово с черточками (заменяем _ на угаданную букву)
                word_as_list = list(word_completion)
                for i in range(len(word)):
                    if word[i] == guess:
                        word_as_list[i] = guess
                word_completion = "".join(word_as_list)
                
                if "_" not in word_completion:
                    guessed = True

        # ВАРИАНТ 2: Пользователь ввел СЛОВО целиком
        elif len(guess) == len(word):
            if guess in guessed_words:
                print(f"⚠️ Вы уже вводили слово: {guess}")
            elif guess != word:
                print(f"❌ Слово '{guess}' неверное.")
                tries -= 1
                guessed_words.append(guess)
            else:
                guessed = True
                word_completion = word
        
        else:
            print(f"❌ Длина слова не совпадает (нужно {len(word)} букв)")

        # Вывод состояния после каждого хода
        print(display_hangman(tries))
        print(word_completion)
        print(f"Осталось попыток: {tries}")
        print()

    # 4. Финал игры
    if guessed:
         print("🎉🎉🎉 ПОЗДРАВЛЯЕМ! Вы угадали слово! 🎉🎉🎉")
         print(f"Загаданное слово: {word}")
    else:
        print("💀 УВЫ, ПОПЫТКИ КОНЧИЛИСЬ! 💀")
        print(f"Загаданное слово было: {word}")
        print("В следующий раз повезет! 👊")

while True:
    # Получаем случайное слово
    target_word = get_word()
    
    # Запускаем игру
    play(target_word)
    
    # Предлагаем сыграть еще раз
    again = input("Хотите сыграть еще раз? (да/нет): ").lower()
    
    if again != "да" and again != "д":
        print("Спасибо за игру! До встречи.")
        break  # Выход из цикла, программа завершается       